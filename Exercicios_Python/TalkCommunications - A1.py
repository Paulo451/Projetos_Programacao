"""
Algoritimo com entrada de dados feita pelo usuário
"""
# Entrada de dados

x = int(input('Digite um valor para X: '))

y = int(input('Digite um valor para Y: '))

# Processamento de dados

z = x + y

# Saída de dados

print(f'O valor de z eh igual a: {z}')

if z % 2 == 0:
    print('Z eh par')
else:
    print('Z eh impar')

