"""
Leia quatro nota, calcule a média aritimética e imprima o resultado
"""

# entrada

n1 = float(input('Digite 4 notas:'))

n2 = float(input())

n3 = float(input())

n4 = float(input())

# processamento

media = (n1 + n2 + n3 + n4)/4

# saída

