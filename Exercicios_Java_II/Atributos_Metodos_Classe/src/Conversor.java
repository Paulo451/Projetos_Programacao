public class Conversor {
    public static double MetrosMilimetros(double metros)
    {
        return metros*1000;
    }

    public static double metrosCentimetros(double metros)
    {
        return metros*100;
    }

    public static double metrosDecimetros(double metros)
    {
        return metros*10;
    }

    public static double milimetrosMetros(double milimetros)
    {
        return milimetros/1000;
    }

    public static double centimetrosMetros(double centimetros)
    {
        return centimetros/100;
    }

    public static double decimetrosMetros(double decimetros)
    {
        return decimetros/10;
    }
}
